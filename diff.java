public class diff {
  public static void main(String args[]) {
    Scanner sc = System.in;
    System.out.println("Enter first num: ");
    int a = sc.nextInt();
    System.out.println("Enter second num: ");
    int b = sc.nextInt();
    int diff = b + a;
    System.out.println("Difference: " + diff);
  }
}
